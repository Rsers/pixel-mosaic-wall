def get_template(name):
    """
    获取指定名称的模板

    参数:
        name (str): 模板名称 ('heart', 'plane', 'balloon')

    返回:
        list: 20x20的二维数组模板，如果名称不存在则返回None
    """
    return TEMPLATES.get(name)


def get_pixel_positions(name):
    """
    获取需要填充的像素位置列表

    参数:
        name (str): 模板名称 ('heart', 'plane', 'balloon')

    返回:
        list: 包含所有值为1的像素坐标的列表，格式为[(row, col), ...]
    """
    template = TEMPLATES.get(name)
    if template is None:
        return []

    positions = []
    for row in range(len(template)):
        for col in range(len(template[row])):
            if template[row][col] == 1:
                positions.append((row, col))

    return positions


